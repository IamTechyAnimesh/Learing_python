
# endswith() function used to check if a string ends with a specific suffix
print(str.endswith("mesh")) # it will return True
print(str.endswith("hello")) # it will return False
# startswith() function used to check if a string starts with a specific prefix
print(str.startswith("hello")) # it will return True
print(str.startswith("animesh")) # it will return False

# capitalize() function used to capitalize the first character of a string
print(str.capitalize()) # it will print Hello animesh
# find() function used to find the index of the first occurrence of a substring in a string
print(str.find("animesh")) # it will print 6