# "\n" is an escape sequence that represents a new line in a string.
a = "hello, i'm animesh and \n welcome to the world of python programming."
print(a)
# "/t" is an escape sequence that represents a tab space in a string.
b = "hello, i'm animesh and \t welcome to the world of python programming."
print(b)
# "\\" is an escape sequence that represents a backslash in a string.
c = "hello, i'm animesh and \\ welcome to the world of python programming."
print(c)
# "\"" is an escape sequence that represents a double quote in a string.
d = "hello, i'm animesh and \"welcome\" to the world of python programming."
print(d)
# "\'" is an escape sequence that represents a single quote in a string.
e = 'hello, i\'m animesh and welcome to the world of python programming.'
print(e)
# "\r" is an escape sequence that represents a carriage return in a string.
f = "hello, i'm animesh and \r welcome to the world of python programming."
print(f)
# "\b" is an escape sequence that represents a backspace in a string.
g = "hello, i'm animesh and \b welcome to the world of python programming."
print(g)
# "\f" is an escape sequence that represents a form feed in a string.
h = "hello, i'm animesh and \f welcome to the world of python programming."
print(h)