#type of Data
'''
1. Integer 
2. Float
3. String
4. Boolean
5. None'''
# this is a example of an integer
my_integer = 42 # integer variable means whole number value
print("this is a example of Integer:", my_integer)

# this is a example of string
my_string = "Hello, World!" # string variable means text value
print("this is a example of String:", my_string)

# this is a example of a float
my_float = 3.14 # float variable means decimal value
print("this is a example of Float:", my_float)  

# this is a example of a boolean
my_boolean = True # boolean variable means true or false
print("this is a example of Boolean:", my_boolean)

# this is a example of None
my_none = None # none variable means koi value nahi hai
print("this is a example of None:", my_none)

# rules for variable name
'''
1. Variable names can contain letters, numbers, and underscores.

2. Variable names must start with a letter or an underscore.

3. Variable names are case-sensitive (myVar and myvar are different).

4. Avoid using keywords or built-in function names as variable names.

5. Use meaningful names that describe the purpose of the variable.
'''
