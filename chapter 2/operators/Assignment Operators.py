# this is Assignment Operators
a = 5
b = 3
print("Initial value of a:", a)
print("Initial value of b:", b)
# using assignment operators
a += 2 # += means a = a + 2
b *= 3 # *= means b = b * 3
print("After using assignment operators:")
print("Updated value of a:", a)
print("Updated value of b:", b)
a -= 1  # -= means a = a - 1
b /= 2 # /= means b = b / 2
print("After using more assignment operators:")
print("Updated value of a:", a)
print("Updated value of b:", b)
a %= 3 # %= means a = a % 3
b //= 2 # //= means b = b // 2
print("After using modulus and floor division assignment operators:")
print("Updated value of a:", a)
print("Updated value of b:", b)
a **= 2 # **= means a = a ** 2
print("After using exponentiation assignment operator:")
print("Updated value of a:", a)
print("Final value of b:", b)