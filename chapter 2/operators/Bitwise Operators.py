# this is Bitwise Operators
a = 5  # In binary: 0101
b = 3  # In binary: 0011
print("Bitwise AND ( & ):", a & b)
print("Bitwise OR ( | ):", a | b)
print("Bitwise XOR ( ^ ):", a ^ b)
print("Bitwise NOT ( ~ ):", ~a)
print("Bitwise left shift ( << ):", a << 1)
print("Bitwise right shift ( >> ):", a >> 1)
# end of Bitwise Operators
# you can use these operators to perform bit-level operations on integers